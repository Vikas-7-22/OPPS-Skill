package SKILL1;

import java.util.Scanner;

public class Conversion {
	public static void main(String args[]) {
		Scanner scanner=new Scanner(System.in);
		int a;
		a=scanner.nextInt();
		long k=a;
		float s=k;
		System.out.println("Integer:"+a);
		System.out.println("long: "+k);
		System.out.println("Float"+s);
		scanner.close();
		
}
}